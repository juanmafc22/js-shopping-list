/*

JavaScript and the Match Object

- Math.sqrt()
Explanation: sqrt() returns the square root of a number.
Example: Math.sqrt(9) returns 3.


- Math.abs()
Explanation: abs() returns the absolute value of a number.
Example: Math.abs(-9) returns 9.

- Math.round()
Explanation: round() returns the value of a number rounded to the nearest integer.
Example: Math.round(9.1) returns 9.

- Math.ceil()
Explanation: ceil() returns the value of a number rounded up to the nearest integer.
Example: Math.ceil(9.1) returns 10.

- Math.floor()
Explanation: floor() returns the value of a number rounded down to the nearest integer.
Example: Math.floor(9.9) returns 9.

- Math.random()
Explanation: random() returns a random number between 0 (inclusive), and 1 (exclusive).
Example: Math.random() returns a random number between 0 and 1.

- Math.min()
Explanation: min() returns the number with the lowest value.
Example: Math.min(1, 2, 3, 4, 5) returns 1.

- Math.max()
Explanation: max() returns the number with the highest value.
Example: Math.max(1, 2, 3, 4, 5) returns 5.

- Math.pow()
Explanation: pow() returns the value of a number raised to the power of another number.
Example: Math.pow(2, 3) returns 8.

*/

